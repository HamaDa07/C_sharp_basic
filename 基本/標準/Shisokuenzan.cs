using System;

namespace Shisokuenzan
{// 演算子で計算してみよう 

    public class Shisokuenzan
    {
        public static void Main()
        {
            var number = 100;
            Console.WriteLine(number);

            var number1 = 100 - 20;
            Console.WriteLine(number1);

            var number2 = 100 * 20;
            Console.WriteLine(number2);

            var number3 = 100 % 20;
            Console.WriteLine(number3);
        }
    }
}
